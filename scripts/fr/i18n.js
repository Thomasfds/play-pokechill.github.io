// ============================================================
// Pokechill — Couche de traduction française
// Fichier : scripts/fr/i18n.js
//
// Rôle : Initialise le namespace de traduction.
//        Chargé après tous les scripts du jeu (après save.js).
//        Ne modifie aucun fichier existant.
//
// Pour désactiver la traduction, passer à false avant le
// chargement de ce fichier (ex: dans la console navigateur) :
//   window.POKECHILL_FR = false;
// ============================================================

if (window.POKECHILL_FR !== false) {
    window.POKECHILL_FR = true;
    console.info('[i18n] Traduction française activée.');
}
